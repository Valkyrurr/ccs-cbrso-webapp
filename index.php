			</h4>
			<h5>
				<strong>MISSION:</strong>
			</h5>
			<p>The mission of CCS-CbRSO unit is to improve the culture of
				research, encourage research development, support research
				publication and encourage community engagement of CCS faculty, staff
				and students.</p>
			<h5>
				<strong>VISION:</strong>
			</h5>
			<h5>
				<strong>Vision 2030-2033</strong>
			</h5>
			<p>To be a leading ASEAN university-based applied research center
				with distinction.</p>
			<h5>
				<strong>Vision 2025</strong>
			</h5>
			<p>To be a leading Philippine university-based applied research
				center with distinction.</p>
			<h5>
				<strong>Vision 2020</strong>
			</h5>
			<p>To be a leading XU or Mindanao university-based applied research
				center with distinction.</p>
			<h5>
				<strong>GOALS:</strong>
			</h5>
			<p>To apply the six CCS areas of specializations (<a href="#tbl2">see Table 2</a>) considering
				University's five thematic areas (i.e. food security, health,
				environment, good governance and peace) by:</p>
			<ul>
				<li class="margin-left"><strong>SG1:</strong> Improving culture of
					research through curriculum integration, trainings, seminars and
					workshops;</li>
				<li class="margin-left"><strong>SG2:</strong> Encouraging
					development of innovative computing applied research through
					multi-disciplinary collaboration within university-based applied
					and basic research units;</li>
				<li class="margin-left"><strong>SG3:</strong> Supporting research
					publication and</li>
				<li class="margin-left"><strong>SG4:</strong> Encouraging community
					engagement through social outreach programs.</li>
			</ul>

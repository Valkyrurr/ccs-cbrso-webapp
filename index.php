			<h4 id="unit-structure">
				<strong>Cb-RSO Unit Structure</strong>
			</h4>
			<p>
				The CCS-Cb-RSO unit structure will be directly under the supervision
				of the CCS Dean. The VP-RSO will oversee the unit's direction and
				thematic areas applied by the unit. The AVP, VP-RSO and CCS Dean
				will together assess the performance evaluation of the unit at the
				end of every school year. The Cb-RSO will have six personnel
				consisting of the following; the <strong>director</strong>, three <strong>program
					coordinators</strong>, one <strong>admin staff</strong> and <strong>student
					assistant/s</strong>.
			</p>
